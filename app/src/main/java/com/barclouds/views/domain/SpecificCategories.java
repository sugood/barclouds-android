package com.barclouds.views.domain;

public class SpecificCategories {
 
	private int specificCategories_id;
	private int allCategories_id;
	private String specificCategories_name;

	public int getSpecificCategories_id() {
		return specificCategories_id;
	}
	public void setSpecificCategories_id(int specificCategoriesId) {
		specificCategories_id = specificCategoriesId;
	}
	public int getAllCategories_id() {
		return allCategories_id;
	}
	public void setAllCategories_id(int allCategoriesId) {
		allCategories_id = allCategoriesId;
	}
	public String getSpecificCategories_name() {
		return specificCategories_name;
	}
	public void setSpecificCategories_name(String specificCategoriesName) {
		specificCategories_name = specificCategoriesName;
	}
	
}
